<h2><span class="mw-headline" id="Requirements">Requirements</span></h2>
<p>Eigen doesn't have any dependencies other than the C++ standard library.
</p><p>We use the CMake build system, but only to build the documentation and unit-tests, and to automate installation. If you just want to use Eigen, you can use the header files right away. There is no binary library to link to, and no configured header file. Eigen is a pure template library defined in the headers.
</p>
