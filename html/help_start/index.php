</p>
<h2><span class="mw-headline" id="Compiler_support">Compiler support</span></h2>
<p>Eigen is standard C++98 and so should theoretically be compatible with any compliant compiler. Whenever we use some non-standard feature, that is optional and can be disabled.
</p><p>Eigen is being successfully used with the following compilers:
</p>
<ul>
<li> <a rel="nofollow" class="external text" href="http://gcc.gnu.org">GCC</a>, version 4.1 and newer. Very good performance with GCC 4.2 and newer.
</li>
<li> <a rel="nofollow" class="external text" href="http://en.wikipedia.org/wiki/Visual_C%2B%2B">MSVC</a> (Visual Studio), 2008 and newer (the old 2.x versions of Eigen support MSVC 2005, but without vectorization).
</li>
<li> <a rel="nofollow" class="external text" href="http://en.wikipedia.org/wiki/Intel_C%2B%2B_Compiler">Intel C++ compiler</a>. Very good performance.
</li>
