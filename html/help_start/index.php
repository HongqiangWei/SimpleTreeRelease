<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<ul>
<li> <b><a href="/index.php?title=Versatility" title="Versatility">Eigen is versatile</a>.</b>
<ul>
<li> It supports all matrix sizes, from small fixed-size matrices to arbitrarily large dense matrices, and even sparse matrices.
</li>
<li> It supports all standard numeric types, including std::complex, integers, and is easily extensible to <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/TopicCustomizingEigen.html#CustomScalarType">custom numeric types</a>.
</li>
<li> It supports various <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/group__TopicLinearAlgebraDecompositions.html">matrix decompositions</a> and <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/group__TutorialGeometry.html">geometry features</a>.
</li>
<li> Its ecosystem of <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/unsupported/index.html">unsupported modules</a> provides many specialized features such as non-linear optimization, matrix functions, a polynomial solver, FFT, and much more.
</li>
</ul>
</li>
<li> <b><a href="/index.php?title=Benchmark" title="Benchmark">Eigen is fast</a>.</b>
<ul>
<li> Expression templates allow to intelligently remove temporaries and enable <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/TopicLazyEvaluation.html">lazy evaluation</a>, when that is appropriate.
</li>
<li> <a href="/index.php?title=FAQ#Vectorization" title="FAQ">Explicit vectorization</a> is performed for SSE 2/3/4, ARM NEON, and AltiVec instruction sets, with graceful fallback to non-vectorized code. 
</li>
<li> Fixed-size matrices are fully optimized: dynamic memory allocation is avoided, and the loops are unrolled when that makes sense.
</li>
<li> For large matrices, special attention is paid to cache-friendliness.
</li>
</ul>
</li>
<li> <b><a href="/index.php?title=Reliability" title="Reliability">Eigen is reliable</a>.</b>
<ul>
<li> Algorithms are carefully selected for reliability. Reliability trade-offs are <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/group__TopicLinearAlgebraDecompositions.html">clearly documented</a> and <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/classEigen_1_1JacobiSVD.html">extremely</a> <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/classEigen_1_1FullPivHouseholderQR.html">safe</a> <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/dox/classEigen_1_1FullPivLU.html">decompositions</a> are available.
</li>
<li> Eigen is thoroughly tested through its own <a href="/index.php?title=Tests" title="Tests">test suite</a> (over 500 executables), the standard BLAS test suite, and parts of the LAPACK test suite.
</li>
</ul>
</li>
<li> <b><a href="/index.php?title=API_Showcase" title="API Showcase">Eigen is elegant</a>.</b>
<ul>
<li> The API is extremely <a href="/index.php?title=API_Showcase" title="API Showcase">clean and expressive</a> while feeling natural to C++ programmers, thanks to expression templates.
</li>
<li> Implementing an algorithm on top of Eigen feels like just copying pseudocode.
</li>
</ul>
</li>
<li> <b><a href="#Compiler_support">Eigen has good compiler support</a></b> as we run our test suite against many compilers to guarantee reliability and work around any compiler bugs. Eigen also is standard C++98 and maintains very reasonable compilation times.
</li>
</ul>
