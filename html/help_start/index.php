</p><p>Earlier versions were licensed under the LGPL3+.
</p><p>Note that currently, a few features rely on third-party code licensed under the LGPL: SimplicialCholesky, AMD ordering, and constrained_cg. Such features can be explicitly disabled by compiling with the EIGEN_MPL2_ONLY preprocessor symbol defined.
Furthermore, Eigen provides interface classes for various third-party libraries (usually recognizable by the &lt;Eigen/*Support&gt; header name). Of course you have to mind the license of the so-included library when using them.
</p><p>Virtually any software may use Eigen. For example, closed-source software may use Eigen without having to disclose its own source code. Many proprietary and closed-source software projects are using Eigen right now, as well as many BSD-licensed projects.
