<h2><span class="mw-headline" id="Mailing_list">Mailing list</span></h2>
<p>Address: <b>eigen@lists.tuxfamily.org</b>
</p>
<ul>
<li> To subscribe, send a mail with subject <i>subscribe</i> to eigen-request@lists.tuxfamily.org
</li>
<li> To unsubscribe, send a mail with subject <i>unsubscribe</i> to eigen-request@lists.tuxfamily.org
</li>
</ul>
<p>The Eigen mailing list can be used for discussing general Eigen development topics. End-user questions are often better asked on the <a rel="nofollow" class="external text" href="http://forum.kde.org/viewforum.php?f=74">Users Forum</a>. Development of specific features is best tracked and discussed on <a rel="nofollow" class="external text" href="http://eigen.tuxfamily.org/bz">our Bugzilla</a>. See <a href="/index.php?title=Bugzilla" title="Bugzilla">this page</a>.
</p><p>This mailing list is <b>public</b> and has <b><a rel="nofollow" class="external text" href="http://listengine.tuxfamily.org/lists.tuxfamily.org/eigen/">public archives</a></b>. The archives are mirrored by <a rel="nofollow" class="external text" href="http://dir.gmane.org/gmane.comp.lib.eigen">gmane.org</a>.
</p><p><b>Important:</b> You must subscribe before you may post. Sorry, this is our only way to prevent spam.
</p><p><b>Important:</b> After you sent your subscription request, you will receive a confirmation e-mail. Check your spam folder, as these confirmation e-mails are often filtered as spam!
</p><p>There is also a <b>private</b> mailing list which should only be used if you want to write privately to a few core developers (it is read by Benoît, Gaël and Jitse). The address is <b>eigen-core-team</b> at the same lists server as for the <a href="#Mailing_list">Eigen mailing list</a>. You do not need to subscribe (actually, subscription is closed). For all Eigen development discussion, use the public mailing list or <a href="/index.php?title=Bugzilla" title="Bugzilla">Bugzilla</a> instead.
</p>
