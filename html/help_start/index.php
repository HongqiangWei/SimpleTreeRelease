<h2><span class="mw-headline" id="Projects_using_Eigen">Projects using Eigen</span></h2>
<p>Feel free to add yourself! If you don't have access to the wiki or if you are not sure about the relevance of your project, ask at the <a href="#Mailing_list">#Mailing list</a>.
</p><p><b>Extensions, numerical computation and bindings</b>
</p>
<ul>
<li> Google's <a rel="nofollow" class="external text" href="http://code.google.com/p/ceres-solver/">Ceres</a> solver is a portable C++ library that allows for modeling and solving large complicated nonlinear least squares problems.
</li>
<li> The Manifold ToolKit <a rel="nofollow" class="external text" href="http://openslam.org/MTK">MTK</a> provides easy mechanisms to enable arbitrary algorithms to operate on manifolds. It also provides a Sparse Least Squares Solver (SLoM) and an Unscented Kalman Filter (UKFoM).
</li>
<li> <a rel="nofollow" class="external text" href="https://borg.cc.gatech.edu/projects/gtsam">GTSAM</a> is a library implementing smoothing and mapping (SAM) in robotics and vision, using factor graphs and Bayes networks.
</li>
<li> <a rel="nofollow" class="external text" href="https://openslam.org/g2o.html">g2o</a> is an open-source C++ framework for optimizing graph-based nonlinear least-square problems.
</li>
<li> <a rel="nofollow" class="external text" href="https://pypi.python.org/pypi/minieigen">minieigen</a> is small boost::python wrapper for Eigen's core classes (dense fixed and dynamic-sized vectors, matrices, aligned boxes, quaternions; with integer, floating and complex scalars) including operators and subset of Eigen's API methods.
</li>
<li> <a rel="nofollow" class="external text" href="http://code.google.com/p/redsvd/">redsvd</a> is a RandomizED Singular Value Decomposition library for sparse or very large dense matrices.
</li>
<li> The <a rel="nofollow" class="external text" href="http://cran.r-project.org/web/packages/RcppEigen/index.html">RcppEigen</a> package provides bindings and more for R.
</li>
<li> <a rel="nofollow" class="external text" href="http://cran.r-project.org/web/packages/trustOptim">trustOptim</a> is a trust-region based non linear solver supporting sparse Hessians (C++ implementation with R binding).
</li>
<li> <a rel="nofollow" class="external text" href="http://packages.python.org/sparray">sparray</a>: a python binding of the Sparse module - alpha stage.
</li>
<li> <a rel="nofollow" class="external text" href="http://shogun-toolbox.org/">Shogun</a>: a large scale machine learning toolbox.
</li>
<li> <a rel="nofollow" class="external text" href="http://mc-stan.org/">Stan</a>: a statistical package based on Eigen that includes a reverse-mode automatic differentiation implementation.
</li>
<li> <a rel="nofollow" class="external text" href="https://github.com/hughperkins/jeigen">jeigen</a>, a Java wrapper for Eigen. 
</li>
<li> <a rel="nofollow" class="external text" href="http://www.autoitscript.com/forum/topic/162921-eigen4autoit-matrix-computing-with-eigen/">Eigen4AutoIt</a>, a wrapper for the AutoIt scripting language.
</li>
<li> <a rel="nofollow" class="external text" href="http://www.ceemple.com">Ceemple</a> is a scientific programming environment allowing to rapidly prototype applications C++ as in MatLab or Python.
</li>
<li> <a rel="nofollow" class="external text" href="https://github.com/marcel-goldschen-ohm/EigenLab">EigenLab</a> is a header only library to parse and evaluate expressions working on Eigen matrices.
</li>
<li> <a rel="nofollow" class="external text" href="http://spafedte.github.io/">SpaFEDte</a> a C++ library for discontinuous Galerkin discretizations on general meshes.
</li>
</ul>
